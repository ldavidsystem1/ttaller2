package zodiaco;

public class Zodiaco {
    public int dia;
    public int mes;
    public int ano;

    public Zodiaco() {
        this.dia = 0;
        this.mes = 0;
        this.ano = 0;
    }
    public int getDia() {
        return dia;
    }
    public int getMes() {
        return mes;
    }
    public int getAno() {
        return ano;
    }
    public void setDia(int dia) {
        this.dia = dia;
    }
    public void setMes(int mes) {
        this.mes = mes;
    }
    public void setAno(int ano) {
        this.ano = ano;
    }
}
