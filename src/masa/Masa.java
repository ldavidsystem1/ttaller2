package masa;

public class Masa {

    public double altura;
    public double peso;


    public Masa() {
        this.altura = 0;
        this.peso = 0;

    }
    public double getAltura() {
        return altura;
    }
    public double getPeso() {
        return peso;
    }
    public void setAltura(double altura) {
        this.altura = altura;
    }
    public void setPeso(double peso) {
        this.peso = peso;
    }

}
